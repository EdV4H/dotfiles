      lualine_b = {},
      lualine_c = {},
      lualine_x = {},
      lualine_y = {},
      lualine_z = {}
    },
  }

  local function ins_a(component)
    table.insert(config.sections.lualine_a, component)
  end
  local function ins_b(component)
    table.insert(config.sections.lualine_b, component)
  end
  local function ins_c(component)
    table.insert(config.sections.lualine_c, component)
  end


  local function ins_x(component)
    table.insert(config.sections.lualine_x, component)
  end
  local function ins_y(component)
    table.insert(config.sections.lualine_y, component)
  end
  local function ins_z(component)
    table.insert(config.sections.lualine_z, component)
  end

  ins_a({
    'mode',
    icon = 'ůĪáĮ',
    separator = { left = 'Óā∂', right = 'Óāī' },
    right_padding = 2,
    color = { gui = 'bold' }
  })

  ins_b({ 'filename', icon = 'Ôďď ' })
  ins_b({
    'diagnostics',
    sources = { 'nvim_diagnostic' },
    symbols = { error = 'Ó™á ', warn = 'Ó©¨ ', info = 'Ó©ī ' },
  })
  ins_b({ 'location', icon = 'ůįćé' })

  ins_x({ 'encoding' })
  ins_x({ 'fileformat' })
  ins_x({ 'filetype' })
  ins_x({ 'datetime', icon = 'Ôźļ', style = '%H:%M' })
  ins_y({
    'diff',
    symbols = { added = 'ÔĎó ', modified = 'ÔĎô ', removed = 'ÔĎė ' },
  })
  ins_z({
    'branch',
    icon = 'ůįė¨',
    color = { gui = 'bold' }
